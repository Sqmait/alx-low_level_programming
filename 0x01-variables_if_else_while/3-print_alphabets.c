#include <stdio.h>
/**
 * main - a program that prints the alphabet in lowercase, and then in uppercase
 *
 * Return 0 (success)
 */
int main(void)
{
	char c;

	char d;

	c = 'a';
	d = 'A';
	while 
		(c <= 'z') {
			putchar(c);
			c++;
		}
	while
		(d <= 'Z') {
			putchar(d);
		}
	putchar('\n');
	return (0);
}
